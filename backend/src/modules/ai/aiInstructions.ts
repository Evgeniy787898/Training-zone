/**
 * Personal instructions interface (PERS-INS-011)
 */
export interface PersonalInstruction {
  type: 'name' | 'address' | 'injury' | 'preference' | 'prohibition' | 'style' | 'note';
  value: string;
  priority?: number;
}

/**
 * Build personal instructions section for system prompt (PERS-INS-011)
 */
export function buildPersonalInstructionsPrompt(instructions: PersonalInstruction[]): string {
  if (!instructions || instructions.length === 0) {
    return '';
  }

  const sections: Record<string, string[]> = {
    name: [],
    address: [],
    injury: [],
    preference: [],
    prohibition: [],
    style: [],
    note: [],
  };

  for (const entry of instructions) {
    if (sections[entry.type]) {
      sections[entry.type].push(entry.value);
    }
  }

  const parts: string[] = ['## üéØ –ü–ï–†–°–û–ù–ê–õ–¨–ù–´–ï –ù–ê–°–¢–†–û–ô–ö–ò:'];

  if (sections.name.length > 0) {
    parts.push(`**–û–±—Ä–∞—â–∞—Ç—å—Å—è:** ${sections.name[0]}`);
  }

  if (sections.address.length > 0) {
    parts.push(`**–°—Ç–∏–ª—å –æ–±—Ä–∞—â–µ–Ω–∏—è:** ${sections.address[0]}`);
  }

  if (sections.injury.length > 0) {
    parts.push(`\n**‚ö†Ô∏è –¢–†–ê–í–ú–´/–û–ì–†–ê–ù–ò–ß–ï–ù–ò–Ø (–ö–†–ò–¢–ò–ß–ù–û!):**`);
    sections.injury.forEach(inj => parts.push(`- ${inj} ‚Üí –ù–ï –ø—Ä–µ–¥–ª–∞–≥–∞—Ç—å —É–ø—Ä–∞–∂–Ω–µ–Ω–∏—è –Ω–∞–≥—Ä—É–∂–∞—é—â–∏–µ —ç—Ç—É –∑–æ–Ω—É!`));
  }

  if (sections.preference.length > 0) {
    parts.push(`\n**–ü—Ä–µ–¥–ø–æ—á—Ç–µ–Ω–∏—è:**`);
    sections.preference.forEach(pref => parts.push(`- ${pref}`));
  }

  if (sections.prohibition.length > 0) {
    parts.push(`\n**üö´ –ù–ï —Ä–µ–∫–æ–º–µ–Ω–¥–æ–≤–∞—Ç—å:**`);
    sections.prohibition.forEach(prohibition => parts.push(`- ${prohibition}`));
  }

  if (sections.style.length > 0) {
    parts.push(`\n**–°—Ç–∏–ª—å –æ–±—â–µ–Ω–∏—è:** ${sections.style[0]}`);
  }

  if (sections.note.length > 0) {
    parts.push(`\n**–ó–∞–º–µ—Ç–∫–∏:**`);
    sections.note.forEach(note => parts.push(`- ${note}`));
  }

  return parts.join('\n');
}

