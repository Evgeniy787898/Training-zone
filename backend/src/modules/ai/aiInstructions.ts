// Personality modes based on user's training consistency
export type PersonalityMode = 'sarcastic' | 'friendly' | 'motivational' | 'coach';

export interface AIPersonalityConfig {
  mode: PersonalityMode;
  missedWorkouts: number;
  streak: number;
  lastWorkoutDaysAgo: number;
}

/**
 * Determine personality mode based on user's training data
 */
export function determinePersonalityMode(config: AIPersonalityConfig): PersonalityMode {
  const { missedWorkouts, streak, lastWorkoutDaysAgo } = config;

  // Sarcastic mode: user has been slacking
  if (lastWorkoutDaysAgo >= 3 || missedWorkouts >= 2) {
    return 'sarcastic';
  }

  // Motivational mode: user is on a streak
  if (streak >= 3) {
    return 'motivational';
  }

  // Friendly is default
  return 'friendly';
}

